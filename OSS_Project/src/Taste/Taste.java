package Taste;

public class Taste {
	
	private String TasteID; //맛 ID
	private String TasteName; //맛 이름(단, 짠, 매운)
	
	public String getTasteID() {
		return TasteID;
	}
	public void setTasteID(String tasteID) {
		TasteID = tasteID;
	}
	public String getTasteName() {
		return TasteName;
	}
	public void setTasteName(String tasteName) {
		TasteName = tasteName;
	}
	
	

}
